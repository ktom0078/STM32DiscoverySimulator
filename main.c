int main(void)
{
	int8_t buff[25];
	float xg,yg,zg;

	/* Set up the system clocks */
	SystemInit();
	LIS3DSH_Init();
	LIS3DSH_Write( 0x20, 0x67 );   //enable x,y,z 100hz output data rate
	LIS3DSH_Write( 0x23, 0xC8 );   //DRY active high on INT1 pin - nem feltetlenul kell

	/* Initialize USB, IO, SysTick, and all those other things you do in the morning */
	init();
	ticker = 0;

	while (1)
	{
		if(ticker == 1000)
		{
			xg = LIS3DSH_Get_X_Out(LIS3DSH_Sense_2g);
			yg = LIS3DSH_Get_Y_Out(LIS3DSH_Sense_2g);
			zg = LIS3DSH_Get_Z_Out(LIS3DSH_Sense_2g);
			sprintf(buff,"x:%.3f,y:%.3f,z:%.3f \r\n",xg,yg,zg);

			VCP_send_str(buff);

			ticker = 0;
		}



	}

	return 0;
}


void init()
{
	GpioInit();

	/* Setup SysTick or CROD! changed to 10ms timing */
	if (SysTick_Config(SystemCoreClock / 10000))
	{
		ColorfulRingOfDeath();
	}


	/* Setup USB */
	USBD_Init(&USB_OTG_dev,
	            USB_OTG_FS_CORE_ID,
	            &USR_desc,
	            &USBD_CDC_cb,
	            &USR_cb);

	return;
}

/*
 * Call this to indicate a failure.  Blinks the STM32F4 discovery LEDs
 * in sequence.  At 168Mhz, the blinking will be very fast - about 5 Hz.
 * Keep that in mind when debugging, knowing the clock speed might help
 * with debugging.
 */
void ColorfulRingOfDeath(void)
{
	uint16_t ring = 1;
	while (1)
	{
		uint32_t count = 0;
		while (count++ < 500000);

		GPIOD->BSRRH = (ring << 12);
		ring = ring << 1;
		if (ring >= 1<<4)
		{
			ring = 1;
		}
		GPIOD->BSRRL = (ring << 12);
	}
}

/*
 * Interrupt Handlers
 */

void SysTick_Handler(void)
{
	ticker++;
	if (downTicker > 0)
	{
		downTicker--;
	}
	if(GpioGetButtonState() == TRUE)
	{
		GpioToggleLed(DISCOVERY_LED_GREEN);
	}

}

void NMI_Handler(void)       {}
void HardFault_Handler(void) { ColorfulRingOfDeath(); }
void MemManage_Handler(void) { ColorfulRingOfDeath(); }
void BusFault_Handler(void)  { ColorfulRingOfDeath(); }
void UsageFault_Handler(void){ ColorfulRingOfDeath(); }
void SVC_Handler(void)       {}
void DebugMon_Handler(void)  {}
void PendSV_Handler(void)    {}

void OTG_FS_IRQHandler(void)
{
  USBD_OTG_ISR_Handler (&USB_OTG_dev);
}

void OTG_FS_WKUP_IRQHandler(void)
{
  if(USB_OTG_dev.cfg.low_power)
  {
    *(uint32_t *)(0xE000ED10) &= 0xFFFFFFF9 ;
    SystemInit();
    USB_OTG_UngateClock(&USB_OTG_dev);
  }
  EXTI_ClearITPendingBit(EXTI_Line18);
}
